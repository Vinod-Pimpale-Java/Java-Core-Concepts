package com.variables;

public class LocalVariables {
	public static void main(String[] args) {
		//NOTE:-
		//Declared inside method body
		// Limited Scope within method only
		// store into stack area
		//they are not static
		//not initialized automatically
		int x; // Declaration of local variables
		int y = 10; // initialization of local variables
		System.out.println("Value of y is>>>" + y); // print value of varibale
	}
}
