package com.operators;

//Note:- Also called as increment and decrement operator
public class UnarayOperator {
	public static void main(String[] args) {
		int a = 10;
		a++;
		System.out.println("Increment value is>>>" + a);
		a--;

		System.out.println("Decrement value is>>>" + a);
		a--;
		a--;
		a--;
		a--;
		System.out.println("Decrement value is>>>" + a);
	}
}
